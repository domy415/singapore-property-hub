// Helper function to get condo by slug
export function getCondoBySlug(slug: string) {
  return condoData[slug as keyof typeof condoData] || null
}

// Helper function to get all condos as array
export function getAllCondos() {
  return Object.values(condoData)
}

// Helper function to get featured condos (top rated)
export function getFeaturedCondos(limit: number = 3) {
  return getAllCondos()
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit)
}